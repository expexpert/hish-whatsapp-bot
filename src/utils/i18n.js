/**
 * Translates a key based on the language.
 * @param {string} key 
 * @param {string} lang 'en' or 'fr'
 * @param {object} params Optional parameters for string replacement
 * @returns {string}
 */
function t(key, lang = 'en', params = {}) {
    const l = (lang === 'fr' || lang === 'en') ? lang : 'en';
    let text = translations[l][key] || translations['en'][key] || key;
    
    // Simple template replacement
    Object.keys(params).forEach(p => {
        text = text.replace(`\${${p}}`, params[p]);
    });
    
    return text;
}

